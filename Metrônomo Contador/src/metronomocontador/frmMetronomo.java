    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed

        
        
//parte principal
        int bpm;
        int nota;
        int tempos;
        
        if (rdbSelecionar.isSelected()) {
            bpm = slideVelo.getValue();
        }
        else {
            bpm = Integer.parseInt(txtVelocidade.getText());
        }
        
        tempos = Integer.parseInt( (String) cboTempos.getSelectedItem());
        nota = Integer.parseInt( (String) cboNota.getSelectedItem());
        
        if (rodando == 1) {
            metro.encerrar();
        }
        
        int quanidadeExercicios = Integer.parseInt(txtQuantidadeExercicio.getText());
        int compassosPorExercicio = Integer.parseInt(txtCompassosExercicio.getText());
        
        txtExerciciosRealizados.setText("0");
        
        metro = new metronomo(bpm, tempos,nota,quanidadeExercicios,compassosPorExercicio,txtExerciciosRealizados);
        metro.run();
        rodando = 1;
    }//GEN-LAST:event_btnIniciarActionPerformed

